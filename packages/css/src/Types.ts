/**
 * Interface describing a CssRenderer function
 */
export type TCssRenderer = () => IRenderedCSSRule;

/**
 * Interface for describing the result of a TCssRenderer function.
 *
 * @export
 * @interface ICssRendererResult
 */
export interface IRenderedCSSRule {
    selector: string;
    css: string;
}

